import math

# -------------

print(math.acos(0.5))  # Inverse cosine of 0.5
print(math.acosh(1.5))  # Inverse hyperbolic cosine of 1.5
print(math.asin(0.5))  # Inverse sine of 0.5
print(math.asinh(0.5))  # Inverse hyperbolic sine of 0.5
print(math.atan(0.5))  # Inverse tangent of 0.5
print(math.atan2(1, 2))  # Inverse tangent of 1/2 in radians
print(math.atanh(0.5))  # Inverse hyperbolic tangent of 0.5
print(math.ceil(0.5))  # Smallest integer greater than or equal to 0.5
print(math.comb(5, 2))  # Number of ways to choose 2 items from 5
print(math.copysign(1, -1))  # Copy sign of -1 to 1
print(math.cos(0.5))  # Cosine of 0.5 radians
print(math.cosh(0.5))  # Hyperbolic cosine of 0.5
print(math.degrees(0.5))  # Convert 0.5 radians to degrees
print(math.dist([1, 2], [3, 4]))  # Euclidean distance between points (1,2) and (3,4)
print(math.erf(0.5))  # Error function of 0.5
print(math.erfc(0.5))  # Complementary error function of 0.5
print(math.exp(0.5))  # Exponential of 0.5
print(math.expm1(0.5))  # Exponential of 0.5 minus 1
print(math.fabs(-0.5))  # Absolute value of -0.5
print(math.factorial(5))  # Factorial of 5
print(math.floor(0.5))  # Largest integer less than or equal to 0.5
print(math.fmod(1, 2))  # Remainder of 1 divided by 2
print(math.frexp(0.5))  # Mantissa and exponent of 0.5
print(math.fsum([1, 2, 3]))  # Sum of [1, 2, 3] with precision
print(math.gamma(0.5))  # Gamma function of 0.5
print(math.gcd(10, 5))  # Greatest common divisor of 10 and 5
print(math.hypot(3, 4))  # Euclidean norm, sqrt(3^2 + 4^2)
print(math.isclose(1, 2))  # Check if 1 is close to 2
print(math.isfinite(0.5))  # Check if 0.5 is finite
print(math.isinf(0.5))  # Check if 0.5 is infinite
print(math.isnan(0.5))  # Check if 0.5 is NaN
print(math.isqrt(5))  # Integer square root of 5
print(math.ldexp(5, 2))  # 5 * (2**2)
print(
    math.lgamma(0.5)
)  # Natural log of the absolute value of the gamma function of 0.5
print(math.log(0.5))  # Natural logarithm of 0.5
print(math.log10(0.5))  # Base-10 logarithm of 0.5
print(math.log1p(0.5))  # Natural logarithm of 1 + 0.5
print(math.log2(0.5))  # Base-2 logarithm of 0.5
print(math.perm(5, 2))  # Number of ways to arrange 2 items from 5
print(math.pow(2, 3))  # 2 raised to the power of 3
print(math.prod([1, 2, 3]))  # Product of [1, 2, 3]
print(math.radians(0.5))  # Convert 0.5 degrees to radians
print(math.remainder(1, 2))  # IEEE 754-style remainder of 1 with 2
print(math.sin(0.5))  # Sine of 0.5 radians
print(math.sinh(0.5))  # Hyperbolic sine of 0.5
print(math.sqrt(0.5))  # Square root of 0.5
print(math.tan(0.5))  # Tangent of 0.5 radians
print(math.tanh(0.5))  # Hyperbolic tangent of 0.5
print(math.trunc(0.5))  # Truncate 0.5 to an integer
