# Variable Assignment
x = 5
y = "John"
print(x)  # Output: 5
print(y)  # Output: John

# ---------

# Reassigning Variables
x = 4
x = "Sally"
print(x)  # Output: Sally

# ---------

# Casting Variables
x = str(3)  # x will be '3'
y = int(3)  # y will be 3
z = float(3)  # z will be 3.0

# ---------

# Getting the Type of Variables
x = 5
y = "John"
print(type(x))  # Output: <class 'int'>
print(type(y))  # Output: <class 'str'>

# ---------

# Single or Double Quotes
# You can use either single or double quotes for strings
x = "John"
# is the same as
x = "John"

# ---------

# Case-Sensitive Variables
a = 4
A = "Sally"
# 'A' will not overwrite 'a'
print(a)  # Output: 4
print(A)  # Output: Sally
